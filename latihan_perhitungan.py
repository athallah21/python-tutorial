## konversi suhu ke  fahrenheit
## Celcius
celcius= float(input("Masukkan Suhu dalam Celsius : "))
print("suhu = ", celcius, "CELCIUS")

## Reamur
reamur=(celcius*4/5) 
print(f"Suhu = {reamur:} REAMUR")

## Farenheit
fahrenheit= (celcius *9/5)+32
print("suhu  =", fahrenheit,"FAHRENHEIT")

## Kelvin
kelvin= celcius + 273
print ("suhu =", kelvin, "KELVIN")

